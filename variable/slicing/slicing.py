# slicing of the string mean getting a small piece from any variable
b="helloworld"
print(b[2:8])
a="school"
print(a[2:4])
c="nice to meet you in the school"
print(c[2:6])

# slice from starting
d="hi i am sameer ali from jandrote"
print(d[:6])

# slice from ends
d="you are good"
print(d[2:])

# negative slicing
d="you are good"
print(d[-5:-1])